CREATE TABLE assemblies
(
    id       int primary key GENERATED BY DEFAULT AS IDENTITY,
    assembly text NOT NULL
);

INSERT INTO assemblies (id, assembly)
VALUES (1, 'Assembly 1'),
       (2, 'Assembly 2'),
       (3, 'Assembly 3'),
